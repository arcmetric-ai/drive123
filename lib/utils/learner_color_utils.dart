int _stableLearnerHash(String input) {
  var hash = 17;
  for (final codeUnit in input.codeUnits) {
    hash = 37 * hash + codeUnit;
    hash &= 0x7fffffff;
  }
  return hash;
}

LearnerColorSet learnerColorForKey(String? rawKey) {
  final key = rawKey?.trim() ?? '';
  if (key.isEmpty) return kLearnerColorLibrary.first;
  final index = _stableLearnerHash(key) % kLearnerColorLibrary.length;
  return kLearnerColorLibrary[index];
}
